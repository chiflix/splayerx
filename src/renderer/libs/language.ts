export function normalizeCode(code: string): LanguageCode {
  if (!code) return LanguageCode.Default;
  return (
    Object.keys(allCodes)
      .find(standardCode => allCodes[standardCode].includes(code)) as LanguageCode
    || LanguageCode.No
  );
}

